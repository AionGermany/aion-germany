package ai.instance.mechanerksWeaponsFactory;

import com.aionemu.gameserver.ai2.AIName;
import com.aionemu.gameserver.model.gameobjects.Creature;
import com.aionemu.gameserver.model.gameobjects.player.Player;
import com.aionemu.gameserver.skillengine.SkillEngine;
import com.aionemu.gameserver.utils.MathUtil;

import ai.AggressiveNpcAI2;

/**
 * @author Falke_34
 */
@AIName("mechanerks_defence_tower")
// 243706
public class MechanerksDefenceTowerAI2 extends AggressiveNpcAI2 {

	@Override
	protected void handleCreatureSee(Creature creature) {
		checkDistance(creature);
	}

	@Override
	protected void handleCreatureMoved(Creature creature) {
		checkDistance(creature);
	}

	private void checkDistance(Creature creature) {
		if (creature instanceof Player) {
			Player player = (Player) creature;
			if (MathUtil.isIn3dRangeLimited(getOwner(), creature, 0, 20)) {
				SkillEngine.getInstance().getSkill(getOwner(), 11117, 65, player).useNoAnimationSkill();
			}
		}
	}
}
